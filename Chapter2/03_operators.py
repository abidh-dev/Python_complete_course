i = 2
y = 3
# arithmetic operators
# Addition
sum_result = i + y
print("Addition:", sum_result)

# Subtraction
sub_result = y - i
print("Subtraction:", sub_result)

# Multiplication
mul_result = i * y
print("Multiplication:", mul_result)

# Division
div_result = y / i
print("Division:", div_result)

# Modulus
mod_result = y % i
print("Modulus:", mod_result)

# logical operators
is_greater = i > y
print("Is i greater than y?:", is_greater)
is_equal = i == y
print("Is i equal to y?:", is_equal)
is_less_equal = i <= y
print("Is i less than or equal to y?:", is_less_equal)
is_not_equal = i != y
print("Is i not equal to y?:", is_not_equal)