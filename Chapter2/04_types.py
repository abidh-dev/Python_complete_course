a = 2
b = 4.6
c = "Hello, Python!"
d = True
print(type(a))  # Output: <class 'int'>
print(type(b))  # Output: <class 'float'>
print(type(c))  # Output: <class 'str'>
print(type(d))  # Output: <class 'bool'>

# Converting integer to float
a_float = float(a)
print(a_float)        # Output: 2.0
print(type(a_float))  # Output: <class 'float'>

# Converting string to float
c_string = "3.14"
c_float = float(c_string)
print(c_float)        # Output: 3.14
print(type(c_float))  # Output: <class 'float'>